CREATE TABLE EMPLOYEE (   
EMPLOYEE_ID INTEGER NOT NULL,   
EMPLOYEE_NAME VARCHAR(35) NOT NULL,   
OFFFICE_NUMBER SMALLINT NOT NULL,   
FLOOR_NUMBER SMALLINT NOT NULL,   
PHONE_NUMBER INTEGER NOT NULL,   
EMAIL_ADDRESS VARCHAR(40) NOT NULL,   
PRIMARY KEY (EMPLOYEE_ID)   
);

CREATE TABLE MEETING(   
MEETING_ID INTEGER NOT NULL,   
EMPLOYEE_ID INTEGER NOT NULL,   
ROOM_NUMBER integer NOT NULL,   
FLOOR_NUMBER INTEGER NOT NULL,   
MEETING_START_TIME INTEGER NOT NULL,   
Constraint PK_MEETING primary key(MEETING_ID,EMPLOYEE_ID) ENABLE 
 
  
);

CREATE TABLE NOTIFICATION(   
NOTIFICATION_ID INTEGER NOT NULL,   
EMPLOYEE_ID INTEGER NOT NULL,   
NOTIFICATION_DATE DATE NOT NULL,   
NOTIFICATION_TYPE VARCHAR(15) NOT NULL,   
PRIMARY KEY(NOTIFICATION_ID)  
);

CREATE TABLE SYMPTOM(   
SYMPTOM_ROW_ID SMALLINT NOT NULL,   
EMPLOYEE_ID INTEGER NOT NULL,   
DATE_REPORTED DATE NOT NULL,   
SYMPTOM_ID SMALLINT NOT NULL,   
PRIMARY KEY(ROW_ID)  
);

CREATE TABLE SCAN_TABLE(   
SCAN_ID INTEGER NOT NULL,   
SCAN_DATE DATE NOT NULL,   
SCAN_TIME INTEGER NOT NULL,   
EMPLOYEE_ID INTEGER NOT NULL,   
TEMPERATURE INTEGER NOT NULL,   
PRIMARY KEY(SACN_ID)  
);

CREATE TABLE TEST_TABLE(   
TEST_ID INTEGER NOT NULL,   
LOCATION1 VARCHAR(15) NOT NULL,   
TEST_DATE DATE NOT NULL,   
TEST_TIME INTEGER NOT NULL,   
EMPLOYEE_ID INTEGER NOT NULL,   
TEST_RESULT VARCHAR(15) NOT NULL,   
Constraint PK_TEST primary key(TEST_ID,EMPLOYEE_ID) ENABLE 
);

CREATE TABLE CASE_TABLE(   
CASE_ID INTEGER NOT NULL,   
EMPLOYEE_ID INTEGER NOT NULL,   
CASE_DATE DATE NOT NULL,   
RESOLUTION VARCHAR(30) NOT NULL,   
Constraint PK_CASE primary key(CASE_ID,EMPLOYEE_ID)  ENABLE 
